locations = ['new york', 'milan', 'sidney', 'malaga', 'las vegas']
print("Original list:")
print(locations)
print("\nSorted list:")
print(sorted(locations))
print("\nOriginal list (unchanged):")
print(locations)

print("\nReversed list:")
locations.reverse()
print(locations)
print("\nOriginal list (unchanged):")
locations.reverse()
print(locations)

