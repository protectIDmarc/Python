person = {'first_name': 'nicole', 'last_name': 'brews-sammons', 'age': 45, 'city': 'johannesburg'}

# Print each piece of information stored in the dictionary
print(f"The person's first name is: {person['first_name'].title()}")
print(f"{person['first_name'].title()}'s last name is: {person['last_name'].title()}")
print(f"{person['first_name'].title()} {person['last_name'].title()} is {person['age']} years old and lives in {person['city'].title()}.")
