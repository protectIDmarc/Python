# Store a name in lowercase
name = "ada lovelace"
# .title() capitalises the first letter of each word: "ada lovelace" -> "Ada Lovelace"
print(name.title())
# print() with no arguments outputs a blank line (useful for spacing)
print()

# .upper() converts the whole string to uppercase: "ada lovelace" -> "ADA LOVELACE"
print(name.upper())
# Another blank line for spacing
print()
# .lower() converts the whole string to lowercase (no visible change here, since it's already lowercase)
print(name.lower())

